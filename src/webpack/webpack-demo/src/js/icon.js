/**
 * 渲染列表公共方法
 * @param {*} selector 选择器
 * @param {*} data 数组数据
 * @param {*} iconName 特定字体的name
 */
function renderIconList(selector, data, iconName) {
  $(selector).html(function(){
    var htmlStr = '';
    $.each(data, function(i, n) {
      htmlStr += '<div class="col-md-2" data-index="'+ i + '">'
                + '<div class="hamburger hamburger-pill">'
                + '<a class="hambur-header" href="javascript:;"><i class="ace-icon ' +  iconName + ' ' + n + '"></i></a>'
                + '<div class="hambur-footer">'
                  + '<span class="text">' + n + '</span>'
                + '</div>'
              + '</div>'
            + '</div>';
    })
    return htmlStr;
  })
};


